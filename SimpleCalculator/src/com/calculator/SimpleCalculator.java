package com.calculator;
//import java.util.*;

public class SimpleCalculator {
	/*This is an addition method which is used to add 2 integers*/
	public static int addition(int x, int y) {
		return x+y;
	}
	/*This is an addition method which is used to minus 2 integers*/
	public static int subtraction(int x, int y) {
		return x-y;
	}
	/*This is an addition method which is used to multiple 2 integers*/
	public static int multiplication(int x, int y) {
		return x*y;
	}
	/*This is an addition method which is used to divide 2 float values*/
	public static float division(int x, int y) {
		return (float)x/(float)y;
	}
	
	}
 
